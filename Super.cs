{
    abstract class Super
    {
        string Name;
        string Power;
        int PowerLevel;

        //<access-modifier>abstract<return-type>method name (parameter)
        public abstract int GetSuperStrength();
        override
            //Formula: 
            SuperStrength = PowerLevel * 2;
            public abstract int GetShrecked();
        override
        //Formula :
        GetShrecked = (PowerLevel / 2);

    }
